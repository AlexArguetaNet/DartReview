void main() {

  // String variable
  var name = "Mario";
  print(name);

  // final key word. Initializes at runtime
  final age = 25;

  // const key word. Compile time constant
  const isOpen = true;

  // Math operations
  print(age + 10);
  print(age - 10);
  print(age * 10);
  print(age / 5);
  print(age % 10);

  // String interpolation
  print("My name is $name and my age is $age");

}